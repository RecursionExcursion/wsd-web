type IconServerParams = {
  iconKey: keyof typeof icons;
  size?: number;
};

const iconServer = (params: IconServerParams) => {
  const { iconKey, size = 50 } = params;

  const icn = icons[iconKey];

  if (!icn) {
    return null;
  }

  // Clone and override its width and height
  const clonedIcon = React.cloneElement(icn, {
    width: size,
    height: size,
  });

  return clonedIcon;
};

export { iconServer };
